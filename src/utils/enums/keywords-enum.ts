/**
 * 定义了关键字的类型。
 * Defination of keywords.
 */
enum Keywords {
    /** 返回。Return. */
    Return = 'return',
    /** 如果。If. */
    If = 'if',
    /** 否则。Else. */
    Else = 'else',
    /** For 循环。For. */
    For = 'for',
    /** While 循环。While. */
    While = 'while',
    /** 整型。Int. */
    Int = 'int',
    /** 空。Void. */
    Void = 'void',
    /** 字符。char */
    Char = 'char',
    /** 长整形 i64(long long) */
    Int64 = 'i64',
    /** 短整型。short */
    Short = 'short',
    /** 操作数大小。Sizeof. */
    Sizeof = 'sizeof',
    /** 结构体。Struct. */
    Struct = 'struct',
    /** 联合体。Union. */
    Union = 'union',
}

export default Keywords;
