enum IntegerType {
    /** 8 位整型。8-bit integer. */
    Int8,
    /** 16 位整型。16-bit integer. */
    Int16,
    /** 32 位整型。32-bit integer. */
    Int32,
    /** 64 位整型。64-bit integer. */
    Int64,
    /** 8 位无符号整型。8-bit unsigned integer. */
    Uint8,
    /** 16 位无符号整型。16-bit unsigned integer. */
    Uint16,
    /** 32 位无符号整型。32-bit unsigned integer. */
    Uint32,
    /** 64 位无符号整型。64-bit unsigned integer. */
    Uint64,
}

export default IntegerType;
